# ASKING USER IF IT IS ELIGIBLE TO VOTE OR NOT
age = int(input("ENTER THE AGE OF PERSON : "))
if age>18:
    print("YOU ARE ELIGIBLE TO VOTE")
else:
    print("YOU ARE NOT ELIGIBLE TO VOTE")

# PROPERTIES OF NUMBER
num = int(input("ENTER THE NUMBER : "))
if num == 0:
    print("ZERO")
elif num>0:
    print("POSITIVE")
else:
    print("NEGATIVE")
